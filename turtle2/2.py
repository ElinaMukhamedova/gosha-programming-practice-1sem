import turtle as t


def f(n):
    x = len(n)
    p = []
    p.append(((0, 0), (-50, 0), (-50, -100), (0, -100), (0, 0)))
    p.append(((0, 0), (-50, -50), (0, 0), (0, -100), (0, 0)))
    p.append(((-50, 0), (0, 0), (0, -50), (-50, -100), (0, -100)))
    p.append(((0, 0), (-50, 0), (0, 0), (-50, -50), (0, -50), (-50, -100)))
    p.append(((0, 0), (0, -100), (0, -50), (-50, -50), (-50, 0)))
    p.append(((0, 0), (-50, 0), (-50, -50), (0, -50), (0, -100), (-50, -100)))
    p.append(((0, 0), (-50, -50), (-50, -100), (0, -100), (0, -50), (-50, -50)))
    p.append(((-50, 0), (0, 0), (-50, -50), (-50, -100)))
    p.append(((0, 0), (-50, 0), (-50, -100), (0, -100),
             (0, -50), (-50, -50), (0, -50), (0, 0)))
    p.append(((0, 0), (-50, 0), (-50, -50), (0, -50),
             (-50, -100), (0, -50), (0, 0)))
    for j in range(x):
        for i in range(len(p[int(n[j])])):
            dx = p[int(n[j])][i][0]
            dy = p[int(n[j])][i][1]
            t.goto(dx + (j)*60, dy)
        t.penup()
        t.goto((j+1)*60, 0)
        t.pendown()


a = input()
f(a)
